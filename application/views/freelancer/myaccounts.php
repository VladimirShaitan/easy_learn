<h3><span style ="color=red"></br>Условия работы (для авторов)</span></h3>
Компания «Легко в учебе» приглашает к сотрудничеству авторов для написания курсовых, бакалаврских дипломных и магистерских работ, рефератов, сочинений, эссе, тезисов, статей, отчетов по практике, контрольных и других видов работ, а также различных творческих заданий для студентов.<br>
Для специалистов мы предлагаем полную или частичную занятость, своевременную выплату гонорара удобным для автора способом.<br>
Не имеет значения Ваше место проживания – все взаимодействие с нами происходит в режиме онлайн посредством функционального Личного кабинета и программ для общения через Интернет.<br>
<h4>Основные требования:</h4><br>
• опыт в написании работ для студентов;<br>
• выполнение работ с уникальностью не менее 60%;<br>
• выполнение доработок;<br>
• быть на связи (телефон, электронная почта, кабинет автора);<br>
• готовность регулярно оценивать и выполнять письменные работы;<br>
• ответственность.<br>
Если Вас заинтересовало наше сотрудничество, добро пожаловать в команду «Легко в учебе»!<br>
<h4>Правила использования сервиса:</h4> <br>
1.  В разделе « ЗАКАЗЫ НА ОЦЕНКУ » находятся актуальные в настоящее время заказы по Вашему профилю, указанному при регистрации.<br>
2.  В разделе «НАЙТИ ЗАКАЗЫ» находятся доступные в данный момент  для всего штата авторов компании «Легко в учебе» проекты.<br>
3.  Раздел проектов является общим для всех авторов фирмы. <br>
4.  Проекты с пометкой «ЗАКАЗЫ НА ОЦЕНКУ», « НАЙТИ ЗАКАЗЫ» необходимо оценить, то есть предложить свою цену работы.<br>
5.  Если Вы первым согласились выполнить проект и менеджер одобрил Вашу ставку – он станет недоступен для остальных участников. <br>
6.  Не берите больше проектов, чем можете выполнить. У нас всегда есть для Вас новые проекты.<br>
<h4>Общение через ЧАТ:</h4><br>
1.  Запрещается передавать свою контактную информацию (телефон, Email и другие ссылки) заказчику.<br>
2.  Запрещается передавать готовые части работ через ЧАТ напрямую заказчику, до появления уведомления «можно передавать части работ».<br>
3.  Готовые работы необходимо передавать в ЧАТ менеджеру<br>
4.  Запрещается игнорировать (не отвечать) письма и вопросы заказчика, менеджера.<br>
<h4>Выполнение заказов:</h4><br>
1.  Необходимо присылать части работы в указанный срок сдачи. При нарушении сроков без предварительного предупреждения, система оштрафует Вас на 10% от стоимости работы. При неоднократном нарушении данного правила блокируется Ваш аккаунт.<br>
2.  В случае нарушения срока и отказа выходить на связь с менеджером, Ваш аккаунт также будет заблокирован.<br>
3.  Запрещается выполнять работу без утверждения плана заказчиком (кроме тех случаев, когда план утвержден и прикреплен к заказу). В случае отказа от работы, без предварительного предупреждения менеджера за несколько дней до ее сдачи, которую Вы подтвердили как выполняемую, Вам будет начислен штраф в размере стоимости этой работы<br>
4.  Работы необходимо загружать по частям следующим образом:<br>
o план (если работа нуждается в его составлении);<br>
o половина работы;<br>
o готовая работа.<br>
5.  Запрещается загружать скопированные полностью из интернета готовые работы.<br>
6.  Запрещается загружать работы, уникальность которых ниже 60% (если в требованиях заказчиком указана уникальность выше, чем 60% - необходимо добиваться требуемого процента). Все работы рекомендуется проверять через бесплатный сервис www.antiplagiat.ru, если не указано другой программы для проверки на уникальность.<br>
7.  В случае некачественного выполнения проекта (высокий уровень плагиата, ошибки в расчетах, несоответствие заданию и методическим требованиям) работа отравится на доработку.
<h4>Проведение выплат</h4> <br>
1.  Оплата за выполненные работы производится через 30 дней после загрузки готового заказа.<br>
2.  За доработки оплата не предусмотрена. Исключение составляют случаи, когда правки выходят за рамки ранее поставленного заказчиком задания.<br>
3.  В том случае, когда на доработке у автора три и более заказа, выплаты замораживаются до выполнения доработок.
